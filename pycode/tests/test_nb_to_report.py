# ---------------------------------------------------------------------------
# Figure floats only emitted when figure_meta provided and file exists
# ---------------------------------------------------------------------------

class TestFigureFloats:
    def test_figure_float_emitted_for_matching_plot(self, tmp_nb_dir):
        (tmp_nb_dir / "plots" / "awesome_plot.png").touch()
        code = "savefig(PLOTS_DIR / \"awesome_plot.png\")"
        nb = _make_nb(_code(code))
        p = tmp_nb_dir / "t.ipynb"
        p.write_text(json.dumps(nb))
        meta = {"awesome_plot.png": ("fig:awesome", "Awesome caption.")}
        _make_extractor(p, tmp_nb_dir, figure_meta=meta).run()
        text = (tmp_nb_dir / "auto_nb_figures.tex").read_text()
        assert r"\includegraphics" in text
        assert "awesome_plot.png" in text

    def test_no_figure_emitted_when_file_missing(self, tmp_nb_dir):
        code = "savefig(PLOTS_DIR / \"missing.png\")"
        nb = _make_nb(_code(code))
        p = tmp_nb_dir / "t.ipynb"
        p.write_text(json.dumps(nb))
        meta = {"missing.png": ("fig:missing", "Cap")}
        _make_extractor(p, tmp_nb_dir, figure_meta=meta).run()
        text = (tmp_nb_dir / "auto_nb_figures.tex").read_text()
        assert r"\includegraphics" not in text

    def test_no_figure_emitted_when_not_in_meta(self, tmp_nb_dir):
        (tmp_nb_dir / "plots" / "unlisted.png").touch()
        code = "savefig(PLOTS_DIR / \"unlisted.png\")"
        nb = _make_nb(_code(code))
        p = tmp_nb_dir / "t.ipynb"
        p.write_text(json.dumps(nb))
        _make_extractor(p, tmp_nb_dir, figure_meta={}).run()
        text = (tmp_nb_dir / "auto_nb_figures.tex").read_text()
        assert r"\includegraphics" not in text


