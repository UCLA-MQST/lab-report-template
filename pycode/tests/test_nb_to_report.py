class TestNotebookExtractorBasic:
    def test_run_creates_fig_tex(self, simple_nb, tmp_nb_dir):
        _make_extractor(simple_nb, tmp_nb_dir).run()
        assert (tmp_nb_dir / "auto_nb_figures.tex").exists()

    def test_run_creates_code_tex(self, simple_nb, tmp_nb_dir):
        _make_extractor(simple_nb, tmp_nb_dir).run()
        assert (tmp_nb_dir / "auto_nb_code.tex").exists()

    def test_run_creates_nb_cells_bundle(self, simple_nb, tmp_nb_dir):
        _make_extractor(simple_nb, tmp_nb_dir).run()
        assert (tmp_nb_dir / "pycode" / "nb_cells" / "nb_cells.py").exists()

    def test_snippet_files_created_for_each_code_cell(self, simple_nb, tmp_nb_dir):
        _make_extractor(simple_nb, tmp_nb_dir).run()
        snippets = list((tmp_nb_dir / "pycode" / "nb_cells").glob("_nb_*.py"))
        assert len(snippets) == 2   # two code cells

    def test_code_tex_contains_lstinputlisting(self, simple_nb, tmp_nb_dir):
        _make_extractor(simple_nb, tmp_nb_dir).run()
        text = (tmp_nb_dir / "auto_nb_code.tex").read_text()
        assert r"\lstinputlisting" in text

    def test_code_tex_has_generated_header(self, simple_nb, tmp_nb_dir):
        _make_extractor(simple_nb, tmp_nb_dir).run()
        text = (tmp_nb_dir / "auto_nb_code.tex").read_text()
        assert "generated by nb_to_report.py" in text


