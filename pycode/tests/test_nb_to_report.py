"""
test_nb_to_report.py
====================
Tests for NotebookExtractor in nb_to_report.py.

Covers:
  - Extract from a minimal well-formed notebook
  - Figure floats rendered for matching plot files
  - Code floats rendered for each code cell
  - Snippet .py files written to nb_cells_dir
  - Runnable bundle (nb_cells.py) written
  - Heading extracted from preceding markdown cell
  - Cells with no code silently skipped
  - Backward-compat shim extract() still works
"""

import json
import sys
import textwrap
from pathlib import Path

import pytest

# Ensure pycode/ is importable regardless of CWD
_pycode = Path(__file__).parent.parent
if str(_pycode) not in sys.path:
    sys.path.insert(0, str(_pycode))

from nb_to_report import NotebookExtractor, extract   # backward-compat


# ---------------------------------------------------------------------------
# Notebook factories
# ---------------------------------------------------------------------------

def _make_nb(*cells: dict) -> dict:
    """Build a minimal notebook dict with nbformat 4."""
    return {"nbformat": 4, "nbformat_minor": 5, "cells": list(cells)}


def _markdown(src: str) -> dict:
    return {"cell_type": "markdown", "source": src, "metadata": {}}


def _code(src: str) -> dict:
    return {"cell_type": "code", "source": src, "metadata": {}, "outputs": []}


# ---------------------------------------------------------------------------
# Fixtures
# ---------------------------------------------------------------------------

@pytest.fixture()
def tmp_nb_dir(tmp_path: Path) -> Path:
    """Return a temp dir that mimics the lab report root layout."""
    (tmp_path / "plots").mkdir()
    (tmp_path / "pycode").mkdir()
    return tmp_path


@pytest.fixture()
def simple_nb(tmp_nb_dir: Path) -> Path:
    nb = _make_nb(
        _markdown("## Setup"),
        _code("import math\nresult = math.pi"),
        _markdown("## Computation"),
        _code("answer = 42\nprint(answer)"),
    )
    p = tmp_nb_dir / "test.ipynb"
    p.write_text(json.dumps(nb), encoding="utf-8")
    return p


def _make_extractor(nb_path: Path, tmp_nb_dir: Path, **kwargs) -> NotebookExtractor:
    """Helper: create a NotebookExtractor with tmp paths."""
    return NotebookExtractor(
        nb_path,
        fig_tex=tmp_nb_dir / "auto_nb_figures.tex",
        code_tex=tmp_nb_dir / "auto_nb_code.tex",
        nb_cells_dir=tmp_nb_dir / "pycode" / "nb_cells",
        plots_dir=tmp_nb_dir / "plots",
        **kwargs,
    )


